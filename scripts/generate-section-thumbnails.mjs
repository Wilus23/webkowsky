import {spawn} from 'node:child_process'
import {mkdir, readdir, rm, writeFile} from 'node:fs/promises'
import net from 'node:net'
import path from 'node:path'
import process from 'node:process'
import {fileURLToPath} from 'node:url'

import {chromium} from 'playwright'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')
const previewPath = '/internal/section-thumbnails'
const generatedDir = path.join(
  repoRoot,
  'studio/static/page-builder-thumbnails/generated',
)
const overridesDir = path.join(
  repoRoot,
  'studio/static/page-builder-thumbnails/overrides',
)
const manifestPath = path.join(
  repoRoot,
  'studio/src/schemaTypes/objects/pageBuilderThumbnailManifest.ts',
)

function normalizeTypeName(fileName) {
  return fileName.replace(/\.png$/i, '')
}

async function listPngTypeNames(directory) {
  try {
    const files = await readdir(directory)
    return files
      .filter((fileName) => fileName.toLowerCase().endsWith('.png'))
      .map(normalizeTypeName)
      .sort()
  } catch (error) {
    if (error && typeof error === 'object' && 'code' in error && error.code === 'ENOENT') {
      return []
    }
    throw error
  }
}

function formatManifestEntries(basePath, typeNames) {
  if (!typeNames.length) return ''

  return typeNames
    .map((typeName) => `    ${JSON.stringify(typeName)}: '${basePath}/${typeName}.png',`)
    .join('\n')
}

async function writeManifest({generatedTypes, overrideTypes}) {
  const generatedEntries = formatManifestEntries(
    '/static/page-builder-thumbnails/generated',
    generatedTypes,
  )
  const overrideEntries = formatManifestEntries(
    '/static/page-builder-thumbnails/overrides',
    overrideTypes,
  )

  const source = `// AUTO-GENERATED by npm run generate:section-thumbnails
export const pageBuilderThumbnailManifest: {
  generated: Record<string, string>
  overrides: Record<string, string>
} = {
  generated: {
${generatedEntries}
  },
  overrides: {
${overrideEntries}
  },
}
`

  await writeFile(manifestPath, source)
}

function wait(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms))
}

function getAvailablePort(startPort) {
  return new Promise((resolve, reject) => {
    const tryPort = (port) => {
      const server = net.createServer()

      server.once('error', () => {
        server.close()
        tryPort(port + 1)
      })

      server.once('listening', () => {
        const address = server.address()
        server.close(() => {
          if (!address || typeof address === 'string') {
            reject(new Error('Unable to determine a free port for thumbnail generation.'))
            return
          }
          resolve(address.port)
        })
      })

      server.listen(port, '127.0.0.1')
    }

    tryPort(startPort)
  })
}

async function launchBrowser() {
  try {
    return await chromium.launch({
      channel: 'chrome',
      headless: true,
    })
  } catch {
    try {
      return await chromium.launch({headless: true})
    } catch (error) {
      throw new Error(
        `Unable to launch a browser for thumbnail generation. Install Google Chrome or run "npx playwright install chromium".\n\n${String(error)}`,
      )
    }
  }
}

function startPreviewServer(port) {
  const output = []
  let exitDetails = null
  const env = {
    ...process.env,
    SECTION_THUMBNAIL_MODE: '1',
  }

  const child = spawn(
    'npm',
    [
      'exec',
      '--workspace=frontend',
      'next',
      'dev',
      '--',
      '--hostname',
      '127.0.0.1',
      '--port',
      String(port),
    ],
    {
      cwd: repoRoot,
      env,
      stdio: ['ignore', 'pipe', 'pipe'],
    },
  )

  const appendOutput = (chunk) => {
    output.push(chunk.toString())
    if (output.length > 40) output.shift()
  }

  child.stdout.on('data', appendOutput)
  child.stderr.on('data', appendOutput)
  child.on('exit', (code, signal) => {
    exitDetails = {code, signal}
  })

  return {
    child,
    hasExited: () => exitDetails !== null,
    getExitDetails: () => exitDetails,
    getLogs: () => output.join(''),
  }
}

async function stopPreviewServer(child) {
  if (!child || child.killed) return

  child.kill('SIGTERM')

  await Promise.race([
    new Promise((resolve) => child.once('exit', resolve)),
    wait(5000).then(() => {
      if (!child.killed) child.kill('SIGKILL')
    }),
  ])
}

async function captureThumbnails(baseUrl) {
  const browser = await launchBrowser()

  try {
    const page = await browser.newPage({
      viewport: {
        width: 1440,
        height: 1200,
      },
      deviceScaleFactor: 1,
    })

    await page.emulateMedia({
      reducedMotion: 'reduce',
      colorScheme: 'light',
    })

    await page.goto(`${baseUrl}${previewPath}`, {waitUntil: 'networkidle'})
    await page.waitForSelector('[data-thumbnail-type]')

    const typeNames = await page.locator('[data-thumbnail-type]').evaluateAll((elements) =>
      elements
        .map((element) => element.getAttribute('data-thumbnail-type'))
        .filter(Boolean),
    )

    for (const typeName of typeNames) {
      const element = page.locator(`[data-thumbnail-type="${typeName}"]`).first()
      await element.scrollIntoViewIfNeeded()
      await element.screenshot({
        path: path.join(generatedDir, `${typeName}.png`),
        animations: 'disabled',
        scale: 'css',
      })
    }

    return typeNames.sort()
  } finally {
    await browser.close()
  }
}

async function main() {
  await mkdir(overridesDir, {recursive: true})
  await rm(generatedDir, {recursive: true, force: true})
  await mkdir(generatedDir, {recursive: true})

  const port = await getAvailablePort(3210)
  const baseUrl = `http://127.0.0.1:${port}`
  const {child, getLogs, hasExited, getExitDetails} = startPreviewServer(port)

  try {
    const previewUrl = `${baseUrl}${previewPath}`
    const timeoutMs = 120000
    const startTime = Date.now()

    while (Date.now() - startTime < timeoutMs) {
      if (hasExited()) {
        const exitDetails = getExitDetails()
        throw new Error(
          `Thumbnail preview server exited before the preview route became ready (code: ${
            exitDetails?.code ?? 'unknown'
          }, signal: ${exitDetails?.signal ?? 'none'}).\n\nRecent server logs:\n${getLogs()}`,
        )
      }

      try {
        const response = await fetch(previewUrl)
        if (response.ok) break
      } catch {
        // Keep polling while Next starts and compiles the route.
      }

      await wait(1000)
    }

    if (Date.now() - startTime >= timeoutMs) {
      throw new Error(`Preview route did not become ready.\n\nRecent server logs:\n${getLogs()}`)
    }

    const generatedTypes = await captureThumbnails(baseUrl)
    const overrideTypes = await listPngTypeNames(overridesDir)
    await writeManifest({generatedTypes, overrideTypes})

    console.log(`Generated ${generatedTypes.length} section thumbnails.`)
    if (overrideTypes.length) {
      console.log(`Detected ${overrideTypes.length} override thumbnail(s).`)
    }
  } finally {
    await stopPreviewServer(child)
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error)
  process.exitCode = 1
})
